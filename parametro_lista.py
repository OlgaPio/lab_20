# Función que retorna la suma de todos los elementos
def suma_lista(lista):
    return sum(lista)

# Función que retorna el mayor valor en la lista
def max_lista(lista):
    return max(lista)

# Función que retorna el menor valor en la lista
def min_lista(lista):
    return min(lista)

# Lista de ejemplo
lista_numeros = [23, 45, 12, 67, 89, 34]

print("Suma de los elementos:", suma_lista(lista_numeros))
print("Mayor valor de la lista:", max_lista(lista_numeros))
print("Menor valor de la lista:", min_lista(lista_numeros))
